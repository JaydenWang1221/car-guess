const maxAttempts = 10;

function countNameLength(name) {
  let length = 0;
  for (let char of name) {
    const code = char.charCodeAt(0);
    if (/[A-Za-z0-9]/.test(char)) {
      length += 1;
    } else if (code >= 0x4E00 && code <= 0x9FFF) {
      length += 1;
    }
  }
  return length;
}

